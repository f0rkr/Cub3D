#include "ft_cub3d.h"

void		ft_max(void)
{
	int i;
	int j;

	i = 0;
	g_data.max = 0;
	while (i < g_data.y_size)
	{
		j = 0;
		while (g_data.map[i][j])
		{
			j++;
		}
		if (j > g_data.max)
			g_data.max = j;
		i++;
	}
}
