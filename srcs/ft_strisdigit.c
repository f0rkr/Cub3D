#include "ft_cub3d.h"

int		ft_strisdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == ' ' || str[i] == '\t')
			i++;
		else if (str[i] < '0' || str[i] > '9')
			return (1);
		else
			i++;
	}
	return (0);
}
